package study.CodingTestBasic.Math;

public class Max {
    public static void main(String[] args) {
        int num1 = 54;
        int num2 = 35;

        System.out.println("num1 = " + num1);
        System.out.println("num2 = " + num2);
        System.out.println("최댓값 : "+Math.max(num1,num2));
    }
}
